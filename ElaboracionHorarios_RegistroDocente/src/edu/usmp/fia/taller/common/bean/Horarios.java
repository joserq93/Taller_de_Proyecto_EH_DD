package edu.usmp.fia.taller.common.bean;

public class Horarios {

	private int idHorario;
	private int ciclo;
	private int codCur;
	private int codLab;
	private String curso;
	private String detalleCurso;
	private int codSec;
	private int numCred;
	public int getIdHorario() {
		return idHorario;
	}
	public void setIdHorario(int idHorario) {
		this.idHorario = idHorario;
	}
	public int getCiclo() {
		return ciclo;
	}
	public void setCiclo(int ciclo) {
		this.ciclo = ciclo;
	}
	public int getCodCur() {
		return codCur;
	}
	public void setCodCur(int codCur) {
		this.codCur = codCur;
	}
	public int getCodLab() {
		return codLab;
	}
	public void setCodLab(int codLab) {
		this.codLab = codLab;
	}
	public String getCurso() {
		return curso;
	}
	public void setCurso(String curso) {
		this.curso = curso;
	}
	public String getDetalleCurso() {
		return detalleCurso;
	}
	public void setDetalleCurso(String detalleCurso) {
		this.detalleCurso = detalleCurso;
	}
	public int getCodSec() {
		return codSec;
	}
	public void setCodSec(int codSec) {
		this.codSec = codSec;
	}
	public int getNumCred() {
		return numCred;
	}
	public void setNumCred(int numCred) {
		this.numCred = numCred;
	}
	
	
}
